-- 1. Selecionar todas as colunas de uma tabela 'clientes'
SELECT * FROM clientes;

-- 2. Selecionar colunas específicas
SELECT nome, email, data_cadastro FROM clientes;

-- 3. Usando WHERE para filtrar resultados
SELECT nome, email FROM clientes
WHERE cidade = 'São Paulo';

-- 4. Usando ORDER BY para ordenar
SELECT nome, data_nascimento FROM clientes
ORDER BY data_nascimento DESC;

-- 5. Usando LIMIT para trazer um número específico de registros
